#include <stdio.h>

/*Informe o tempo gasto numa viagem (em horas), a velocidade média e calcule o consumo.*/

int main(){
float tg,vm,dis,cm,cResu;
printf("qual foi o tempo gasto em horas:\n");
scanf("%f",&tg);
printf("qual foi a velocidade media:\n");
scanf("%f",&vm);
printf("qual foi o consumo medio:\n");
scanf("%f",&cm);
dis = tg * vm;
printf("o consumo foi mais ou menos %.2f",cResu = dis / cm);
}
